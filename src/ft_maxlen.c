#include "header.h"

int		ft_maxlen(char **list)
{
	int	i;
	int	j;
	int	len;

	len = 0;
	i = 1;
	j = 0;
	while (list[i] != '\0')
	{
		while (list[i][j] != '\0')
		{
			if (j > len)
				len = j;
			j++;
		}
		i++;
		j = 0;
	}
	return (len + 1);
}
